                            de
                            sus etapas. <strong style="color: #b8860b">(</strong>Generación, movimiento interno de
                            residuos, almacenamiento, transporte y
                            disposición final<strong style="color: #b8860b">)</strong>.
                        </li>
                        <br>
                        <li>
                            Desarrollar estrategias de formación y educación para incentivar a la adecuada separación en
                            la
                            fuente de los residuos generados en la entidad, con el propósito de optimizar los residuos
                            aprovechables, y así disminuir el volumen de residuos depositados en el centro de acopio.
